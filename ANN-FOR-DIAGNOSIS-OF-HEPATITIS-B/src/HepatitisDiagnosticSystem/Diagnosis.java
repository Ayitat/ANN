    private void jbtnDiagnoseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDiagnoseActionPerformed
        // TODO add your handling code here:
        String status = jtxtDiseaseStatus.getText();
        
        if(jTxtLastName.getText().equals("") && jTxtFirstName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please, Select a Patient");
       }
        else{
            if(hbsAg.equals("YES") && hbeAg.equals("YES") && antiVHD.equals("NO") && antiVHC.equals("NO")){
                    jtxtDiseaseStatus.setText("Positive");
                    jbtnGenerateReport.setVisible(false);
                    jbtnGenerateSeverity.setVisible(true);
            
                }
            
                else{
                    jtxtDiseaseStatus.setText("Negative");
                    jbtnGenerateReport.setVisible(true);
                    jbtnGenerateSeverity.setVisible(false);
                  
                    }
        }
    }//GEN-LAST:event_jbtnDiagnoseActionPerformed

