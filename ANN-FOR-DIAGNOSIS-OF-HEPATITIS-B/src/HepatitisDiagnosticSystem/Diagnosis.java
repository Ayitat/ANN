    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         
        int row = jTable1.getSelectedRow();
        int column = jTable1.getSelectedColumn();
        String tableClick = jTable1.getModel().getValueAt(row, column).toString();
        try{
                            Class.forName(driver);
                            Connection con = DriverManager.getConnection(url,user,pass);
                            Statement stm = con.createStatement();
                            
                            String sql = "SELECT `LastName`, `FirstName` FROM `patientrecord`  WHERE `PatientId` = "+tableClick+"";
                                         
                            ResultSet rs = stm.executeQuery(sql);
                            
                            if (rs.next()){
                                
                                jTxtLastName.setText(rs.getString("LastName"));
                                jTxtFirstName.setText(rs.getString("FirstName"));
                             
                            }
	
			}catch(Exception e){
				  Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, e);
			}
    
    }//GEN-LAST:event_jTable1MouseClicked

